    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        int mayor= 0, mx = 0, my = 0;
        txtVlrx.setText("");
        txtVlry.setText("");
        txtArea.setText("");
        
        for( int x=0;x<vlrX;x++){
            for(int y=0;y<vlrY;y++){
                if(x==0 && y==0){
                    mayor = matriz[x][y];
                    mx = x;
                    my = y;
                }else{
                    if(matriz[x][y] > mayor){
                        mayor = matriz[x][y];
                        mx = x;
                        my = y;
                    }
                }
            
            } 
        }
        
       
        txtArea.setText("El Valor mas alto en la matriz es: "+ String.valueOf(mayor) + " En la posicion: " + String.valueOf(mx) + "," + String.valueOf(my));
    }//GEN-LAST:event_btnBuscarActionPerformed


