    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        int random = 0, result = 0;
        vlrX = Integer.valueOf(txtVlrx.getText());
        vlrY = Integer.valueOf(txtVlry.getText());
        
        matriz = new int[vlrX][vlrY];
        for(int j=0;j<vlrX;j++){
            for(int k=0;k<vlrY;k++){
                result = Integer.valueOf(JOptionPane.showInputDialog("Digite el valor entero de la posicion [" + j + "] [" + k + "]"));
                matriz[j][k] = result;
                random ++;
            } 
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

