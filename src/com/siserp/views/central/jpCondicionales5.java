    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        // TODO add your handling code here:
        double numero1, numero2, numero3;
        String mensaje = "";
        numero1 = Double.valueOf(txtNumero1.getText());
        numero2 = Double.valueOf(txtNumero2.getText());
        numero3 = Double.valueOf(txtNumero3.getText());
        
        if((numero1 > numero2 && numero1 < numero3) || (numero1 > numero3 && numero1 < numero2)){
            mensaje = "El primer número: "+ numero1 + " es el numero intermedio";
        }
        if((numero2 > numero1 && numero2 < numero3) || (numero2 > numero3 && numero2 < numero1)){
            mensaje = "El segundo número: "+ numero2 + " es el numero intermedio";
        }
        if((numero3 > numero1 && numero3 < numero2) || (numero3 > numero2 && numero3 < numero1)){
            mensaje = "El tercero número: "+ numero3 + " es el numero intermedio";
        }
        
        areaResultado.append(mensaje);
    }//GEN-LAST:event_btnGenerarActionPerformed


